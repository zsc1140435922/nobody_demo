public class UserTwo {

    private String name;
    private Integer age;

    /**
     * 获取name
     *
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * 设置name
     *
     * @param the name
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * 获取age
     *
     * @return the age
     */
    public Integer getAge() {
        return age;
    }

    /**
     * 设置age
     *
     * @param the age
     */
    public void setAge(Integer age) {
        this.age = age;
    }
}
